# Digite uma metragem e veja isso em cm e mm:

print('Descubra o valor de uma metragem em tanto em centímetros quanto em milímetros')
metragem = float(input('Digite uma metragem em metros(m): '))
cm = metragem*100
mm = metragem*1000
print(f'O valor que foi fornecido, quando convertido para centimetros é igual a: {cm}.\nO valor que foi fornecido, quando convertido para milimetros é igual a: {mm}.')